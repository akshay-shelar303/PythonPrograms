marks=float(input("Enter the marks="))
if marks>=80:
    print("Distiction")

elif marks>=65 and marks<80:
    print("first class")

elif marks>=55 and marks<65:
    print("Higher Second class")

elif marks>=45 and marks<55:
    print("Scond class")

elif marks>=35 and marks<45:
    print("Third class")
elif marks<35:
    print("fail")
